<?php
require_once '../config.php';
global $conn;

if (!isLoggedIn('admin')) {
    redirect('login.php');
}

$message = '';
$message_type = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $name = sanitize($_POST['name']);
    $description = sanitize($_POST['description']);
    $price = (float)$_POST['price'];
    $duration_days = (int)$_POST['duration_days'];

    if (empty($name) || empty($description) || $price <= 0 || $duration_days <= 0) {
        $message = 'All fields are required and must be valid.';
        $message_type = 'danger';
    } else {
        $stmt = $conn->prepare("INSERT INTO service_plans (name, description, price, duration_days) VALUES (?, ?, ?, ?)");
        $stmt->bind_param("ssdi", $name, $description, $price, $duration_days);

        if ($stmt->execute()) {
            $_SESSION['admin_message'] = 'Service plan added successfully.';
            $_SESSION['admin_message_type'] = 'success';
            redirect('service_plans.php');
        } else {
            $message = 'Error adding service plan: ' . $conn->error;
            $message_type = 'danger';
        }
        $stmt->close();
    }
}

